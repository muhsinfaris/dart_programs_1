void main() {
  String size = 'L';
  if (size == 'XS') {
    print('Shirt size is XS');
  } else if (size == 'S') {
    print('Shirt size is S');
  } else if (size == 'M') {
    print('Shirt size is M');
  } else if (size == 'L') {
    print('Shirt size is L');
  } else {
    print('Size is not available');
  }
}